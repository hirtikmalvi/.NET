using System;

namespace Practice_3
{
    internal class MathOperations
    {
        public int Add(int n1, int n2)
        {
            return n1 + n2;
        }
        public int Add(int n1, int n2, int n3)
        {
            return n1 + n2 + n3;
        }
        public double Add(double n1, double n2)
        {
            return n1 + n2;
        }
    }

}
